def suma_tres_enteros(a, b, c):
    return a + b + c

# Ejemplo de uso
num1 = int(input("Ingresa el primer número entero: "))
num2 = int(input("Ingresa el segundo número entero: "))
num3 = int(input("Ingresa el tercer número entero: "))

resultado = suma_tres_enteros(num1, num2, num3)
print("La suma de los tres enteros es:", resultado)

